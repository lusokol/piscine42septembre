#include <unistd.h>

char	ft_putchar(char a)
{
	write(1, &a, 1);
}

char	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}
