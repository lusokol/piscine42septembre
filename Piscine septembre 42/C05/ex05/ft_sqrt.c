int		ft_sqrt(int nb)
{
	int		result;

	if (nb < 1 || nb > 2147395600)
		return (0);
	result = 0;
	while (result <= 46340)
	{
		if (result * result == nb)
			return (result);
		result++;
	}
	return (0);
}
